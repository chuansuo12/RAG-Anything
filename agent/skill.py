# Skill 3 — ListDiscoverSkill
# =============================================================================

class ListDiscoverSkill(BaseSkill):
    """
    Discover all item names within a list-typed schema field.

    Returns a SkillResult whose ``discovered_items`` contains the names;
    does NOT write anything to disk (that is left to ItemDetailSkill).
    The SkillSupervisor uses the discovered names to fan out ItemDetailSkill
    invocations in parallel.
    """

    name = "list_discover"
    description = (
        "Discover all item names in a list-type schema field using a sub-agent. "
        "Returns the list of names for downstream ItemDetailSkill invocations."
    )

    async def execute(self, ctx: SkillContext, **kwargs) -> SkillResult:
        # Use the first item template from the list as the schema example.
        item_template = (
            (ctx.schema_fragment or [{}])[0]
            if isinstance(ctx.schema_fragment, list)
            else {}
        )
        schema_str = json.dumps(item_template, ensure_ascii=False, indent=2)
        key_title = ctx.target_key.replace("_", " ").title()

        system_prompt = SKILL_LIST_DISCOVER_PROMPT.format(
            KEY_TITLE=key_title,
            TARGET_KEY=ctx.target_key,
            SCHEMA_FRAGMENT=schema_str,
        )
        task = (
            f"Identify all items in the '{ctx.target_key}' category from the "
            f"knowledge base. Return ONLY a JSON object: "
            f'{{"items": ["name1", "name2", ...]}}'
        )
        tool_names = ["kb_query", "kb_chunk_query"]
        tool_map = _build_tool_map(ctx)

        async def _do(attempt: int = 0) -> List[str]:
            output = await _run_sub_agent(
                ctx.llm, tool_map,
                system_prompt, task,
                tool_names, ctx.sub_agent_timeout,
            )
            items = _parse_items(output, ctx.target_key)
            if not items:
                raise ValueError(
                    f"Discovery returned zero items for '{ctx.target_key}'"
                )
            return items

        try:
            items = await _with_retry(
                _do,
                label=f"list_discover/{ctx.target_key}",
                max_retries=ctx.max_retries,
            )
            logger.info(
                "ListDiscoverSkill: found %d items in '%s': %s",
                len(items), ctx.target_key, items,
            )
            return SkillResult(
                success=True,
                discovered_items=items,
                summary=f"Discovered {len(items)} items in '{ctx.target_key}'",
            )
        except Exception as exc:
            logger.error("ListDiscoverSkill failed for '%s': %s", ctx.target_key, exc)
            return SkillResult(success=False, discovered_items=[], summary=str(exc))


