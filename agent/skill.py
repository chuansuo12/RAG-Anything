from __future__ import annotations

"""
Schema-driven Skill architecture for domain knowledge extraction.

This module introduces a **Skill** abstraction that sits between high-level
orchestration and low-level tools:

    Schema (arbitrary JSON)
          ↓
    SkillSupervisor               (orchestrator — drives skill selection)
          ↓  activates
    Skills                        (parameterised, reusable extraction units)
          ↓  use internally
    Sub-Agents + Tools            (kb_query, write_json_file, …)
          ↓  write to
    tmp_dir/{key}.json            (scalar fields)
    tmp_dir/{key}/{item}.json     (list-category items)
          ↓
    MergeSkill                    (pure Python — combines all outputs)

Designed to run **alongside** the existing ProductInfoPipeline (pipeline.py).
The pipeline is specialised for the product schema; this module handles
arbitrary schemas via schema-driven skill decomposition.

Directory layout created by SkillSupervisor::

    {tmp_dir}/
      product.json             ← ScalarExtractSkill output
      components/              ← ListDiscoverSkill + ItemDetailSkill outputs
        body.json
        strap.json
        …
      features/
        spo2_monitoring.json
        …

Key design properties
---------------------
- **Parameterised**: each skill receives a schema_fragment at runtime.
- **Reusable**: the same ItemDetailSkill handles any list category.
- **Composable**: SkillSupervisor combines skills via a SkillPlan.
- **Reliable**: retry + text-based recovery (same pattern as pipeline.py).
- **Schema-agnostic**: supports any JSON schema, not just the product schema.
"""

import asyncio
import json
import logging
import re
import shutil
import time
from abc import ABC, abstractmethod
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional, Type

