# Skill 2 — ScalarExtractSkill
# =============================================================================

class ScalarExtractSkill(BaseSkill):
    """
    Extract a scalar or dict-typed top-level schema field.

    Launches a focused sub-agent that queries the knowledge base and writes
    the result to ``{tmp_dir}/{target_key}.json`` via the write_json_file tool.
    """

    name = "scalar_extract"
    description = (
        "Extract a scalar or structured (dict) schema field from the knowledge base "
        "using a sub-agent. Writes the result to a JSON file."
    )

    async def execute(self, ctx: SkillContext, **kwargs) -> SkillResult:
        target_path = ctx.tmp_dir / f"{ctx.target_key}.json"
        schema_str = json.dumps(ctx.schema_fragment, ensure_ascii=False, indent=2)
        key_title = ctx.target_key.replace("_", " ").title()

        system_prompt = SKILL_SCALAR_EXTRACT_PROMPT.format(
            KEY_TITLE=key_title,
            TARGET_KEY=ctx.target_key,
            TARGET_FILE=str(target_path),
            SCHEMA_FRAGMENT=schema_str,
        )
        task = (
            f"Extract the '{ctx.target_key}' information from the knowledge base "
            f"and write it to: {target_path}"
        )
        tool_names = ["kb_query", "kb_chunk_query", "write_json_file"]
        tool_map = _build_tool_map(ctx)

        async def _do(attempt: int = 0) -> None:
            output = await _run_sub_agent(
                ctx.llm, tool_map,
                system_prompt, _strengthen(task, attempt),
                tool_names, ctx.sub_agent_timeout,
            )
            if not target_path.exists():
                _try_recover(output, target_path)
            if not target_path.exists():
                raise _FileNotWrittenError(target_path)

        try:
            await _with_retry(
                _do,
                label=f"scalar_extract/{ctx.target_key}",
                max_retries=ctx.max_retries,
            )
            return SkillResult(
                success=True,
                output_path=target_path,
                summary=f"Extracted '{ctx.target_key}' → {target_path.name}",
            )
        except Exception as exc:
            logger.error("ScalarExtractSkill failed for '%s': %s", ctx.target_key, exc)
            return SkillResult(success=False, summary=str(exc))


