class ItemDetailSkill(BaseSkill):
    """
    Extract detailed information for a single discovered item.

    Launched once per item by the SkillSupervisor (in parallel, bounded by
    a semaphore).  Writes result to
    ``{tmp_dir}/{target_key}/{item_name}.json``.

    Requires the keyword argument ``item_name`` at call time.
    """

    name = "item_detail"
    description = (
        "Extract detailed information for a specific list item from the knowledge base. "
        "Writes result to a JSON file inside the category subdirectory."
    )

    async def execute(self, ctx: SkillContext, *, item_name: str, **kwargs) -> SkillResult:
        cat_dir = ctx.tmp_dir / ctx.target_key
        cat_dir.mkdir(parents=True, exist_ok=True)
        target_path = cat_dir / _safe_filename(item_name)

        item_template = (
            (ctx.schema_fragment or [{}])[0]
            if isinstance(ctx.schema_fragment, list)
            else {}
        )
        schema_str = json.dumps(item_template, ensure_ascii=False, indent=2)
        key_title = ctx.target_key.replace("_", " ").title()

        system_prompt = SKILL_ITEM_DETAIL_PROMPT.format(
            KEY_TITLE=key_title,
            TARGET_KEY=ctx.target_key,
            ITEM_NAME=item_name,
            TARGET_FILE=str(target_path),
            SCHEMA_FRAGMENT=schema_str,
        )
        task = (
            f"Extract detailed information for '{ctx.target_key}' item '{item_name}' "
            f"and write it to: {target_path}"
        )
        tool_names = [
            "kb_query", "kb_chunk_query", "kb_page_context", "write_json_file",
        ]
        tool_map = _build_tool_map(ctx)

        async def _do(attempt: int = 0) -> None:
            output = await _run_sub_agent(
                ctx.llm, tool_map,
                system_prompt, _strengthen(task, attempt),
                tool_names, ctx.sub_agent_timeout,
            )
            if not target_path.exists():
                _try_recover(output, target_path)
            if not target_path.exists():
                raise _FileNotWrittenError(target_path)

        try:
            await _with_retry(
                _do,
                label=f"item_detail/{ctx.target_key}/{item_name}",
                max_retries=ctx.max_retries,
            )
            return SkillResult(
                success=True,
                output_path=target_path,
                summary=f"Extracted '{ctx.target_key}/{item_name}'",
            )
        except Exception as exc:
            logger.error(
                "ItemDetailSkill failed for '%s/%s': %s",
                ctx.target_key, item_name, exc,
            )
            return SkillResult(success=False, summary=str(exc))


