def _safe_filename(name: str) -> str:
    """Turn an arbitrary item name into a safe JSON file stem."""
    stem = re.sub(r"[^\w\-]", "_", name.strip())[:120]
    return (stem or "unnamed") + ".json"


def _build_tool_map(ctx: SkillContext) -> Dict[str, BaseTool]:
    all_tools = ctx.rag_tools + [ctx.write_tool]
    return {t.name: t for t in all_tools}


async def _run_sub_agent(
    llm: BaseChatModel,
    tool_map: Dict[str, BaseTool],
    system_prompt: str,
    task: str,
    tool_names: List[str],
    timeout: float = 180.0,
) -> str:
    """Spin up a fresh sub-agent, run it with a timeout, and return text output."""
    tools = [tool_map[n] for n in tool_names if n in tool_map]
    if not tools:
        raise ValueError(f"No valid tools found for names: {tool_names}")

    agent = create_agent(model=llm, tools=tools, system_prompt=system_prompt)
    messages = [{"role": "user", "content": task}]
    config = {"max_concurrency": 3, "recursion_limit": 50}

    coro = asyncio.to_thread(agent.invoke, {"messages": messages}, config=config)
    result = await asyncio.wait_for(coro, timeout=timeout)

    text = get_last_ai_message_content(result)
    return text or str(result)


async def _with_retry(fn, *, label: str = "", max_retries: int = 2) -> Any:
    """Call ``fn(attempt=N)`` with exponential-backoff retry."""
    last_exc: Optional[Exception] = None
    for attempt in range(max_retries + 1):
        try:
            return await fn(attempt=attempt)
        except Exception as exc:
            last_exc = exc
            if attempt < max_retries:
                wait = min(2 ** attempt * 2, 30)
                logger.warning(
                    "[%s] attempt %d/%d failed (%s). Retrying in %ds …",
                    label, attempt + 1, max_retries + 1, exc, wait,
                )
                await asyncio.sleep(wait)
    raise last_exc  # type: ignore[misc]


def _strengthen(task: str, attempt: int) -> str:
    """Prepend a stronger directive on retry attempts."""
    if attempt == 0:
        return task
    return (
        "CRITICAL: Your previous attempt FAILED because you did NOT call "
        "the write_json_file tool. You MUST call it this time to write "
        "the result to the specified file path.\n\n" + task
    )


def _try_recover(text: str, target_path: Path) -> bool:
    """
    If the sub-agent returned JSON in its text reply instead of calling
    write_json_file, extract the JSON and write the file ourselves.
    """
    obj = _try_parse_json_object(text)
    if obj is None:
        return False
    try:
        target_path.parent.mkdir(parents=True, exist_ok=True)
        target_path.write_text(
            json.dumps(obj, ensure_ascii=False, indent=2),
            encoding="utf-8",
        )
        logger.info("Recovered JSON from agent text → %s", target_path)
        return True
    except OSError as exc:
        logger.warning("Recovery write failed for %s: %s", target_path, exc)
        return False


def _try_parse_json_object(text: str) -> Optional[Dict[str, Any]]:
    """Find the first balanced ``{...}`` JSON object in text (handles code fences)."""
    if not text:
        return None
    cleaned = re.sub(r"```(?:json)?\s*", "", text)
    cleaned = re.sub(r"```", "", cleaned)

    brace_depth = 0
    start: Optional[int] = None
    for i, ch in enumerate(cleaned):
        if ch == "{":
            if brace_depth == 0:
                start = i
            brace_depth += 1
        elif ch == "}":
            brace_depth -= 1
            if brace_depth == 0 and start is not None:
                candidate = cleaned[start : i + 1]
                try:
                    obj = json.loads(candidate)
                    if isinstance(obj, dict):
                        return obj
                except json.JSONDecodeError:
                    pass
                start = None
    return None


def _parse_items(text: str, category: str) -> List[str]:
    """Best-effort extraction of an item list from a sub-agent text response."""
    if not text:
        return []

    for key in ("items", category):
        pattern = r'\{\s*"' + re.escape(key) + r'"\s*:\s*(\[.*?\])\s*\}'
        m = re.search(pattern, text, re.DOTALL)
        if m:
            try:
                arr = json.loads(m.group(1))
                return [str(x).strip() for x in arr if str(x).strip()]
            except json.JSONDecodeError:
                pass

    m = re.search(r"\[.*?\]", text, re.DOTALL)
    if m:
        try:
            arr = json.loads(m.group())
            if arr and all(isinstance(x, str) for x in arr):
                return [x.strip() for x in arr if x.strip()]
        except json.JSONDecodeError:
            pass

    bullets = re.findall(r"(?:^|\n)\s*(?:[-*]|\d+\.)\s+(.+)", text)
    if bullets:
        return [b.strip().strip('"').strip("'") for b in bullets if b.strip()]

    return []


