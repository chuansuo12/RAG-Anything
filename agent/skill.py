# SkillSupervisor
# =============================================================================

class SkillSupervisor:
    """
    Schema-driven supervisor that orchestrates extraction skills.

    Workflow::

        1. SchemaAnalyzeSkill  →  parse schema, generate SkillPlan
        2. For each SkillStep in plan:
              ScalarExtractSkill  →  extract dict-typed fields
              ListDiscoverSkill   →  discover item names in list fields
                └─ ItemDetailSkill (×N, parallel)  →  extract each item
        3. MergeSkill           →  combine all JSON files into final result

    Unlike the ProductInfoPipeline which hard-codes the product schema steps,
    SkillSupervisor works with **any** JSON schema by deriving the execution
    plan at runtime via SchemaAnalyzeSkill.

    Usage::

        supervisor = SkillSupervisor(doc_meta, schema, llm)
        result = await supervisor.run()
        supervisor.cleanup()
    """

    def __init__(
        self,
        doc_meta: Dict[str, Any],
        schema: Dict[str, Any],
        llm: BaseChatModel,
        *,
        max_retries: int = 2,
        sub_agent_timeout: float = 180.0,
        max_concurrency: int = 3,
        verbose: bool = False,
    ) -> None:
        self.doc_meta = doc_meta
        self.schema = schema
        self.llm = llm
        self.max_retries = max_retries
        self.sub_agent_timeout = sub_agent_timeout
        self.max_concurrency = max_concurrency
        self.verbose = verbose

        # Isolated tmp directory for this supervisor run.
        runtime_root = Path(doc_meta.get("working_dir", ".")).resolve().parent
        self._tmp_dir = runtime_root / "skill_tmp"
        if self._tmp_dir.exists():
            shutil.rmtree(self._tmp_dir, ignore_errors=True)
        self._tmp_dir.mkdir(parents=True, exist_ok=True)
        logger.info("SkillSupervisor tmp_dir: %s", self._tmp_dir)

        self._rag_tools = build_rag_agent_tools(doc_meta)
        self._write_tool = WriteJsonFileTool(
            allowed_directory=str(self._tmp_dir)
        )

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    async def run(self) -> Dict[str, Any]:
        """Execute the full skill pipeline and return the merged result."""
        t0 = time.perf_counter()

        # Step 1 — Schema analysis (pure Python, no LLM)
        plan_result = await SchemaAnalyzeSkill().execute(
            self._make_ctx("__schema__", self.schema)
        )
        plan: SkillPlan = plan_result.data

        # Step 2 — Execute each planned step
        for step in plan.steps:
            ctx = self._make_ctx(step.target_key, step.schema_fragment)

            if step.skill_cls is ScalarExtractSkill:
                await ScalarExtractSkill().execute(ctx)

            elif step.skill_cls is ListDiscoverSkill:
                discover_result = await ListDiscoverSkill().execute(ctx)
                items = discover_result.discovered_items or []
                if items:
                    await self._run_item_detail_parallel(ctx, items)

        # Step 3 — Merge (pure Python, no LLM)
        merge_result = await MergeSkill().execute(
            self._make_ctx("__merge__", self.schema)
        )
        result: Dict[str, Any] = merge_result.data or {}

        elapsed = time.perf_counter() - t0
        logger.info(
            "SkillSupervisor finished in %.1fs  keys=%s  tmp_dir=%s",
            elapsed, list(result.keys()), self._tmp_dir,
        )
        return result

    def cleanup(self) -> None:
        """Remove the tmp directory. Call after you are done with the result."""
        if self._tmp_dir.exists():
            shutil.rmtree(self._tmp_dir, ignore_errors=True)

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _make_ctx(self, target_key: str, schema_fragment: Any) -> SkillContext:
        return SkillContext(
            doc_meta=self.doc_meta,
            schema=self.schema,
            schema_fragment=schema_fragment,
            target_key=target_key,
            tmp_dir=self._tmp_dir,
            llm=self.llm,
            rag_tools=self._rag_tools,
            write_tool=self._write_tool,
            max_retries=self.max_retries,
            sub_agent_timeout=self.sub_agent_timeout,
            max_concurrency=self.max_concurrency,
        )

    async def _run_item_detail_parallel(
        self, ctx: SkillContext, item_names: List[str]
    ) -> None:
        """Fan out ItemDetailSkill for every discovered item, bounded by semaphore."""
        sem = asyncio.Semaphore(self.max_concurrency)

        async def _one(name: str) -> None:
            async with sem:
                await ItemDetailSkill().execute(ctx, item_name=name)

        results = await asyncio.gather(
            *[_one(n) for n in item_names],
            return_exceptions=True,
        )
        for name, res in zip(item_names, results):
            if isinstance(res, Exception):
                logger.error(
                    "ItemDetailSkill failed for '%s/%s': %s",
                    ctx.target_key, name, res,
                )


