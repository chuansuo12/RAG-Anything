# Skill 5 — MergeSkill  (pure Python, no LLM)
# =============================================================================

class MergeSkill(BaseSkill):
    """
    Merge all JSON files written by extraction skills into a final result dict.

    Merge rules (schema-driven):
    - ``{tmp_dir}/{key}.json``       →  result[key] = dict
    - ``{tmp_dir}/{key}/``           →  result[key] = [list of dicts]

    Pure Python — no LLM call.  This is always the final step.
    """

    name = "merge"
    description = (
        "Merge all extraction outputs into a single structured result dict. "
        "Scalar JSON files become dict values; category directories become lists."
    )

    async def execute(self, ctx: SkillContext, **kwargs) -> SkillResult:
        result: Dict[str, Any] = {}

        for key in ctx.schema.keys():
            scalar_path = ctx.tmp_dir / f"{key}.json"
            cat_dir = ctx.tmp_dir / key

            if scalar_path.exists():
                try:
                    result[key] = json.loads(
                        scalar_path.read_text(encoding="utf-8")
                    )
                except (json.JSONDecodeError, OSError) as exc:
                    logger.warning("MergeSkill: failed to read %s: %s", scalar_path, exc)

            elif cat_dir.is_dir():
                items: List[Dict[str, Any]] = []
                for f in sorted(cat_dir.glob("*.json")):
                    try:
                        obj = json.loads(f.read_text(encoding="utf-8"))
                        if isinstance(obj, dict):
                            items.append(obj)
                        elif isinstance(obj, list):
                            items.extend(x for x in obj if isinstance(x, dict))
                    except (json.JSONDecodeError, OSError) as exc:
                        logger.warning("MergeSkill: failed to read %s: %s", f, exc)
                if items:
                    result[key] = items

        logger.info("MergeSkill: merged keys=%s", list(result.keys()))
        return SkillResult(
            success=True,
            data=result,
            summary=f"Merged {len(result)} top-level keys: {list(result.keys())}",
        )


