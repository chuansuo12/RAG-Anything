#!/usr/bin/env python
"""
RAGAnything Eval Script
=======================

功能：
- 从 parquet / csv 文件中读取 eval 数据（包含 doc_id / query / Answer）
- 根据 docid 加载对应的 LightRAG workspace：runtime/source/{docid}/rag_storage
- 使用 RAGAnything 对每条 query 生成回答
- 使用给定 Prompt 调用评估专用 LLM（QWEN_PLUS_3_5 + enable_thinking）做自动打分（accuracy: 0/1）
- 统计整体准确率，并将明细保存到输出文件（parquet / jsonl）

依赖：
- pandas（用于读写 parquet）
- tqdm

用法示例：
  cd /Users/tengyujia/ml_project_3/RAG-Anything
  python eval/raganything_eval.py \
    --dataset_path runtime/eval/train-00000-of-00001.parquet \
    --source_root runtime/source \
    --limit 100

  使用 v2 知识库评测（需先对 source 下文档运行 scripts/generate_v2_storage.py 生成 rag_storage_v2）：
  python eval/raganything_eval.py \
    --dataset_path runtime/eval/train-00000-of-00001.parquet \
    --source_root runtime/source \
    --use_v2 \
    --limit 100
"""

