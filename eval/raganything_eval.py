EVAL_PROMPT_TEMPLATE = """You are an expert evaluator tasked with assessing the accuracy of answers generated by a RAG
(Retrieval-Augmented Generation) system.

**Task**: Evaluate whether the generated answer correctly responds to the given question based on the expected answer.

**Question**: {question}

**Expected Answer**: {expected_answer}

**Generated Answer**: {generated_answer}

**Evaluation Criteria**:
1. **Accuracy (0 or 1)**: Does the generated answer match the factual content of the expected answer?
   - 1: The generated answer is factually correct and aligns with the expected answer
   - 0: The generated answer is factually incorrect or contradicts the expected answer

**Instructions**:
- Focus on factual correctness, not writing style or format
- Consider partial matches: if the generated answer contains the correct information but includes additional context, it should still be considered accurate
- For numerical answers, check if the values match or are equivalent
- For list answers, check if all key elements are present
- If the expected answer is "Not answerable" and the generated answer indicates inability to answer, consider it accurate

**Output Format**:
Please respond with a JSON object containing only:
{{
  "accuracy": 0 or 1,
  "reasoning": "Brief explanation of your evaluation"
}}
"""


@dataclass
class EvalConfig:
    dataset_path: Path
    source_root: Path
    docid_col: str = "doc_id"
    question_col: str = "query"
    answer_col: str = "Answer"
    query_mode: str = "hybrid"  # LightRAG mode: "local" / "global" / "hybrid" / "mix" 等
    limit: Optional[int] = None
    docid_filter: Optional[List[str]] = None
    use_agent: bool = False  # When True, use Q&A Agent pipeline instead of rag.aquery
    use_v2: bool = False  # When True, use v2 knowledge base (rag_storage_v2) for evaluation


def build_doc_ragstore_mapping(
    source_root: Path,
    output_path: Path = DOC_RAGSTORE_PATH,
    use_v2: bool = False,
) -> Dict[str, str]:
    """
    扫描 runtime/source 下所有 meta.json，构建：
        评测集 doc_id (即 meta.json 中的 file_name) -> rag_storage working_dir
    的映射，并写入 eval/doc_ragstore.json。

    use_v2=True 时使用 v2 知识库（working_dir_v2 或由 working_dir 推导的 rag_storage_v2），
    且仅当该目录存在时才加入映射。

    返回内存中的映射 dict，形如：
        {"xxx.pdf": "/abs/path/to/.../rag_storage", ...}
    """
    mapping: Dict[str, str] = {}

    if not source_root.exists():
        logger.warning("source_root does not exist, skip building doc_ragstore: %s", source_root)
        return mapping

    # 遍历所有一级子目录下的 meta.json
    for meta_path in source_root.glob("*/meta.json"):
        try:
            data = json.loads(meta_path.read_text(encoding="utf-8"))
        except Exception as e:  # noqa: BLE001
            logger.warning("Failed to read meta.json %s: %s", meta_path, e)
            continue

        file_name = data.get("file_name")
        working_dir = data.get("working_dir")

        if not file_name or not working_dir:
            logger.warning("meta.json missing file_name/working_dir: %s", meta_path)
            continue

        if use_v2:
            v2_dir = (data.get("working_dir_v2") or "").strip()
            if not v2_dir:
                v2_dir = resolve_working_dir_v2(str(working_dir))
            if not Path(v2_dir).exists():
                logger.warning(
                    "use_v2=True but v2 dir missing for doc %s: %s",
                    file_name,
                    v2_dir,
                )
                continue
            mapping[str(file_name)] = str(v2_dir)
        else:
            mapping[str(file_name)] = str(working_dir)

    try:
        output_path.parent.mkdir(parents=True, exist_ok=True)
        output_path.write_text(json.dumps(mapping, ensure_ascii=False, indent=2), encoding="utf-8")
        logger.info(
            "doc_ragstore mapping built with %d entries and written to %s",
            len(mapping),
            output_path,
        )
    except Exception as e:  # noqa: BLE001
        logger.warning("Failed to write doc_ragstore.json to %s: %s", output_path, e)

    return mapping


def build_doc_meta_mapping(
    source_root: Path,
    use_v2: bool = False,
) -> Dict[str, Dict[str, Any]]:
    """
    Scan runtime/source/*/meta.json and build a mapping from doc_id (file_name)
    to the full doc_meta dict needed by the Q&A Agent tools.

    use_v2=True 时在 doc_meta 中设置 kb_version="v2" 与 working_dir_v2，
    以便 Agent 工具使用 v2 知识库检索。

    Returns:
        {"xxx.pdf": {"working_dir": "...", "parsed_dir": "...", "doc_id": "...", ...}, ...}
    """
    mapping: Dict[str, Dict[str, Any]] = {}
    if not source_root.exists():
        return mapping

    for meta_path in source_root.glob("*/meta.json"):
        try:
            data = json.loads(meta_path.read_text(encoding="utf-8"))
        except Exception:  # noqa: BLE001
            continue

        file_name = data.get("file_name")
        working_dir = data.get("working_dir")
        if not file_name or not working_dir:
            continue

        entry = {
            "working_dir": str(working_dir),
            "parsed_dir": str(data.get("parsed_dir") or ""),
            "doc_id": str(data.get("doc_id") or ""),
        }
        if use_v2:
            v2_dir = (data.get("working_dir_v2") or "").strip()
            if not v2_dir:
                v2_dir = resolve_working_dir_v2(str(working_dir))
            entry["working_dir_v2"] = v2_dir
            entry["kb_version"] = "v2"
        mapping[str(file_name)] = entry

    return mapping


def build_eval_prompt(question: str, expected_answer: str, generated_answer: str) -> str:
    """构造用于自动评估的 Prompt 文本。"""
    return EVAL_PROMPT_TEMPLATE.format(
        question=question,
        expected_answer=expected_answer,
        generated_answer=generated_answer,
    )


def load_eval_dataset(dataset_path: Path) -> pd.DataFrame:
    """
    读取评测数据集，支持：
    - .parquet（pandas.read_parquet）
    - .csv（pandas.read_csv）
    """
    suffix = dataset_path.suffix.lower()
    if suffix in {".parquet", ".pq"}:
        return pd.read_parquet(dataset_path)
    if suffix == ".csv":
        # 评测列通常是字符串；禁用 NA 解析以避免空字符串变成 NaN
        return pd.read_csv(dataset_path, dtype=str, na_filter=False)
    raise ValueError(
        f"Unsupported dataset file type: {dataset_path} (supported: .parquet/.pq/.csv)"
    )


def _extract_json_from_text(text: str) -> str:
    """
    从模型输出中尽量提取 JSON 字符串。

    有时模型会包一层 ```json ... ```，这里做一点鲁棒处理。
    """
    text = text.strip()
    # 去掉代码块包裹
    if text.startswith("```"):
        # 按第一行语言标记切掉
        parts = text.split("```", 2)
        if len(parts) >= 2:
            text = parts[1].strip()
            # 如果仍然包含语言前缀（如 json\n{...}）
            if "\n" in text:
                first, rest = text.split("\n", 1)
                if first.lower() in {"json", "javascript"}:
                    text = rest.strip()

    # 尝试从第一个 '{' 到最后一个 '}' 截取
    start = text.find("{")
    end = text.rfind("}")
    if start != -1 and end != -1 and end > start:
        return text[start : end + 1]
    return text


def parse_eval_result(raw_text: str) -> Dict[str, Any]:
    """
    将 LLM 输出解析为 {"accuracy": 0/1, "reasoning": "..."}。
    若解析失败，则返回 accuracy=0 并附上原因。
    """
    try:
        json_str = _extract_json_from_text(raw_text)
        data = json.loads(json_str)
        acc = int(data.get("accuracy", 0))
        if acc not in (0, 1):
            acc = 0
        reasoning = str(data.get("reasoning", "")).strip()
        return {"accuracy": acc, "reasoning": reasoning or "No reasoning provided"}
    except Exception as e:  # noqa: BLE001
        logger.warning(
            "Failed to parse eval JSON: %s; raw_model_output(truncated)=%r",
            e,
            raw_text[:500],
        )
        return {
            "accuracy": 0,
            "reasoning": f"Failed to parse eval JSON: {e}",
        }


async def get_rag_for_docid(
    docid: str,
    cfg: EvalConfig,
    cache: Dict[str, RAGAnything],
    doc_ragstore: Dict[str, str],
) -> Optional[RAGAnything]:
    """
    按 docid 加载/复用对应的 RAGAnything 实例。

    约定：
    - 在评测前，由 build_doc_ragstore_mapping 基于 runtime/source/*/meta.json
      构建了 doc_id -> rag_storage working_dir 的映射（存储于 eval/doc_ragstore.json）
    - 这里根据样本中的 docid（即 meta.json 的 file_name 字段）查找对应的 working_dir
    - 该目录中的内容由前置流水线（MinerU + 图谱构建）已准备好
    """
    if docid in cache:
        return cache[docid]

    workspace_str = doc_ragstore.get(docid)
    if not workspace_str:
        logger.error(
            "Workspace mapping not found for docid=%s in doc_ragstore (path=%s)",
            docid,
            DOC_RAGSTORE_PATH,
        )
        return None

    workspace = Path(workspace_str)
    if not workspace.exists():
        logger.error("Workspace not found for docid=%s: %s", docid, workspace)
        return None

    rag_cfg = RAGAnythingConfig(
        working_dir=str(workspace),
        # 评估阶段不需要重新解析，只复用现有图谱
        parse_method="auto",
        parser="mineru",
        display_content_stats=False,
    )

    rag = RAGAnything(
        config=rag_cfg,
        llm_model_func=llm_model_func_eval,
        vision_model_func=vision_model_func,
        embedding_func=embedding_func,
        lightrag_kwargs={
            "rerank_model_func": qwen_rerank_model_func,
        },
    )

    init_result = await rag._ensure_lightrag_initialized()  # type: ignore[attr-defined]
    if not init_result or not init_result.get("success", True):
        logger.error(
            "Failed to initialize LightRAG for docid=%s: %s",
            docid,
            init_result.get("error") if isinstance(init_result, dict) else init_result,
        )
        return None

    cache[docid] = rag
    return rag


async def evaluate_single_row(
    row: pd.Series,
    cfg: EvalConfig,
    rag_cache: Dict[str, RAGAnything],
    doc_ragstore: Dict[str, str],
    doc_meta_mapping: Optional[Dict[str, Dict[str, Any]]] = None,
) -> Optional[Dict[str, Any]]:
    """对单条样本运行 RAGAnything + 评价 LLM，返回评估结果 dict。"""
    docid = str(row[cfg.docid_col])
    question = str(row[cfg.question_col])
    expected_answer = str(row[cfg.answer_col])

    # ---------- Agent path ----------
    if cfg.use_agent and doc_meta_mapping:
        doc_meta = doc_meta_mapping.get(docid)
        if doc_meta is None:
            logger.error("doc_meta not found for docid=%s (agent path)", docid)
            return None

        try:
            agent_result = await run_qa_agent(
                question=question,
                doc_meta=doc_meta,
                max_retries=2,
            )
            generated_answer = agent_result.get("answer", "")
            logger.info(
                "Agent answered docid=%s q_type=%s confidence=%s attempts=%s",
                docid,
                agent_result.get("question_type"),
                agent_result.get("confidence"),
                agent_result.get("attempts"),
            )
        except Exception as e:  # noqa: BLE001
            logger.error("Q&A Agent failed for docid=%s: %s", docid, e)
            return {
                "docid": docid,
                "question": question,
                "expected_answer": expected_answer,
                "generated_answer": "",
                "accuracy": 0,
                "reasoning": f"Q&A Agent failed: {e}",
            }
    else:
        # ---------- Baseline path (rag.aquery) ----------
        rag = await get_rag_for_docid(docid, cfg, rag_cache, doc_ragstore)
        if rag is None:
            return None

        try:
            generated_answer = await rag.aquery(question, mode=cfg.query_mode)
        except Exception as e:  # noqa: BLE001
            logger.error("RAGAnything query failed for docid=%s: %s", docid, e)
            return {
                "docid": docid,
                "question": question,
                "expected_answer": expected_answer,
                "generated_answer": "",
                "accuracy": 0,
                "reasoning": f"RAGAnything query failed: {e}",
            }

    # 3. 构造评估 Prompt 并调用 LLM 打分（使用 eval 专用 llm：QWEN_PLUS_3_5 + enable_thinking）
    prompt = build_eval_prompt(
        question=question,
        expected_answer=expected_answer,
        generated_answer=str(generated_answer),
    )

    try:
        raw_eval = llm_model_func_eval(
            prompt,
            None,
            None,
        )
        if asyncio.iscoroutine(raw_eval):
            raw_eval = await raw_eval
    except Exception as e:  # noqa: BLE001
        logger.error("Eval LLM call failed for docid=%s: %s", docid, e)
        return {
            "docid": docid,
            "question": question,
            "expected_answer": expected_answer,
            "generated_answer": str(generated_answer),
            "accuracy": 0,
            "reasoning": f"Eval LLM call failed: {e}",
        }

    eval_data = parse_eval_result(str(raw_eval))

    return {
        "docid": docid,
        "question": question,
        "expected_answer": expected_answer,
        "generated_answer": str(generated_answer),
        "accuracy": int(eval_data.get("accuracy", 0)),
        "reasoning": eval_data.get("reasoning", ""),
    }


async def run_eval(cfg: EvalConfig) -> None:
    """主评估流程。"""
    if not cfg.dataset_path.exists():
        raise FileNotFoundError(f"Dataset not found: {cfg.dataset_path}")

    # 为本次评测创建独立的运行目录，并把日志写入其中
    run_root = Path("runtime/eval")
    timestamp = datetime.now().strftime("%Y-%m-%d-%H-%M-%S")
    run_dir = run_root / timestamp
    run_dir.mkdir(parents=True, exist_ok=True)

    log_path = run_dir / "eval.log"
    file_handler = logging.FileHandler(log_path, encoding="utf-8")
    file_handler.setFormatter(
        logging.Formatter("%(asctime)s [%(levelname)s] %(name)s: %(message)s")
    )
    root_logger = logging.getLogger()
    root_logger.addHandler(file_handler)

    logger.info("Eval run directory initialized at %s", run_dir)

    # 先构建（或刷新）doc_id -> rag_storage working_dir 的映射
    logger.info(
        "Building doc_id -> rag_storage mapping from %s (use_v2=%s)",
        cfg.source_root,
        cfg.use_v2,
    )
    doc_ragstore = build_doc_ragstore_mapping(
        cfg.source_root, DOC_RAGSTORE_PATH, use_v2=cfg.use_v2
    )

    logger.info("Loading dataset from %s", cfg.dataset_path)
    df = load_eval_dataset(cfg.dataset_path)

    required_cols = [cfg.docid_col, cfg.question_col, cfg.answer_col]
    missing_cols = [c for c in required_cols if c not in df.columns]
    if missing_cols:
        raise KeyError(
            "Dataset is missing required columns: "
            f"{missing_cols}. Available columns: {list(df.columns)}"
        )

    # 若指定了 docid_filter，仅保留这些 docid 对应的样本
    if cfg.docid_filter:
        docid_set = {str(d).strip() for d in cfg.docid_filter if str(d).strip()}
        if docid_set:
            before = len(df)
            df = df[df[cfg.docid_col].astype(str).isin(docid_set)]
            logger.info(
                "Applied docid_filter, rows: %d -> %d (unique docids=%d)",
                before,
                len(df),
                len(docid_set),
            )

    if cfg.limit is not None and cfg.limit > 0:
        df = df.head(cfg.limit)

    logger.info("Loaded %d rows for evaluation", len(df))

    # Build doc_meta mapping for agent path
    doc_meta_mapping: Optional[Dict[str, Dict[str, Any]]] = None
    if cfg.use_agent:
        doc_meta_mapping = build_doc_meta_mapping(cfg.source_root, use_v2=cfg.use_v2)
        logger.info("Agent mode enabled. doc_meta mapping has %d entries.", len(doc_meta_mapping))

    rag_cache: Dict[str, RAGAnything] = {}
    results: List[Dict[str, Any]] = []

    eval_mode = "agent" if cfg.use_agent else "baseline"
    for _, row in tqdm(df.iterrows(), total=len(df), desc=f"Evaluating ({eval_mode})"):
        res = await evaluate_single_row(
            row, cfg, rag_cache, doc_ragstore, doc_meta_mapping=doc_meta_mapping,
        )
        if res is not None:
            results.append(res)

    if not results:
        logger.warning("No evaluation results produced.")
        return

    # 统计准确率
    total = len(results)
    correct = sum(int(r.get("accuracy", 0)) for r in results)
    accuracy = correct / total if total > 0 else 0.0

    logger.info("Eval finished: %d samples, correct=%d, accuracy=%.4f", total, correct, accuracy)
    print(f"\nEval finished: {total} samples, correct={correct}, accuracy={accuracy:.4f}\n")

    # 保存结果到按时间戳划分的目录：runtime/eval/yyyy-MM-dd-HH-mm-ss
    out_df = pd.DataFrame(results)

    # 明细：CSV
    details_path = run_dir / "details.csv"
    out_df.to_csv(details_path, index=False)

    # 汇总：Markdown
    summary_path = run_dir / "summary.md"
    summary_md = (
        f"# RAGAnything Eval Summary\n\n"
        f"- **Dataset**: `{cfg.dataset_path}`\n"
        f"- **Samples**: **{total}**\n"
        f"- **Correct**: **{correct}**\n"
        f"- **Accuracy**: **{accuracy:.4f}**\n"
        f"- **Query mode**: `{cfg.query_mode}`\n"
        f"- **Use agent**: `{cfg.use_agent}`\n"
        f"- **Use v2 KB**: `{cfg.use_v2}`\n"
        f"- **Doc ID column**: `{cfg.docid_col}`\n"
        f"- **Question column**: `{cfg.question_col}`\n"
        f"- **Answer column**: `{cfg.answer_col}`\n"
    )
    summary_path.write_text(summary_md, encoding="utf-8")

    logger.info("Per-run eval outputs written to %s", run_dir)


def parse_args() -> EvalConfig:
    parser = argparse.ArgumentParser(
        description="Evaluate RAGAnything on a parquet/csv dataset using accuracy metric.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )

    parser.add_argument(
        "--dataset_path",
        type=str,
        default="runtime/eval/train-00000-of-00001.parquet",
        help="Path to dataset file (.parquet/.csv) containing columns: doc_id, query, Answer (or specify *_col args).",
    )
    parser.add_argument(
        "--source_root",
        type=str,
        default="runtime/source",
        help="Root directory where per-docid workspaces are stored (runtime/source/{docid}/rag_storage).",
    )
    parser.add_argument(
        "--docid_col",
        type=str,
        default="doc_id",
        help="Column name for document id in the dataset.",
    )
    parser.add_argument(
        "--question_col",
        type=str,
        default="question",
        help="Column name for question in the dataset.",
    )
    parser.add_argument(
        "--answer_col",
        type=str,
        default="answer",
        help="Column name for expected answer in the dataset.",
    )
    parser.add_argument(
        "--query_mode",
        type=str,
        default="hybrid",
        help="LightRAG query mode passed to RAGAnything.aquery (e.g., local / global / hybrid / mix).",
    )
    parser.add_argument(
        "--docid_filter",
        type=str,
        default=None,
        help="Optional comma-separated list of doc_ids to evaluate (e.g., id1,id2,id3).",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=None,
        help="Optional limit on number of samples to evaluate.",
    )
    parser.add_argument(
        "--use_agent",
        action="store_true",
        default=False,
        help="Use Q&A Agent pipeline (retrieval + verification + retry) instead of rag.aquery.",
    )
    parser.add_argument(
        "--use_v2",
        action="store_true",
        default=False,
        help="Use v2 knowledge base (rag_storage_v2) for evaluation. Requires v2 index generated (e.g. via scripts/generate_v2_storage.py).",
    )

    args = parser.parse_args()

    docid_filter: Optional[List[str]] = None
    if args.docid_filter:
        docid_filter = [x.strip() for x in args.docid_filter.split(",") if x.strip()]

    return EvalConfig(
        dataset_path=Path(args.dataset_path),
        source_root=Path(args.source_root),
        docid_col=args.docid_col,
        question_col=args.question_col,
        answer_col=args.answer_col,
        query_mode=args.query_mode,
        limit=args.limit,
        docid_filter=docid_filter,
        use_agent=args.use_agent,
        use_v2=args.use_v2,
    )


def main() -> None:
    logging.basicConfig(
        level=logging.INFO,
        format="%(levelname)s: %(message)s",
    )
    cfg = parse_args()
    asyncio.run(run_eval(cfg))


if __name__ == "__main__":
    main()

